vector<int> sum_numbers() {
  vector<int> acc(60, 0);
  int acci;
  int acc_len = acc.size();

  int numi;
  int num_len = numbers[0].size();

  int tmp;
  int carryi;

  for (int i=0; i<num_len ; i++) {
    numi = num_len-1-i;
    acci = acc_len-1-i;
    tmp = acc[acci];

    for (auto num : numbers) {
      tmp += num[numi];
    }

    // at minimum, set acc[acci] to tmp's ones-place
    // shift tmp right, and add the next digit of acc to the left
    // repeat until tmp is 0
    carryi = 0;
    while (tmp > 0) {
      acc[acci-carryi] = tmp%10;
      tmp = (tmp/10)+acc[acci-carryi-1];
      carryi++;
    }
  }

  return acc;
}

// returns the first n digits of the summed numbers
string first_n_from_sum(int n) {
  vector<int> sum = sum_numbers();
  string first_n(n, '0');
  bool started = false;

  for (int i=0; i<sum.size(); i++) {
    if (sum[i] == 0) continue;

    // The first non-zero digit indicates the start of digits.
    for (int j=0; j<n; j++) {
      first_n[j] = '0'+sum[i+j];
    }
    break;
  }

  return first_n;
}

